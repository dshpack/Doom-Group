#include "inc/libft.h"

void	ft_putnbr(int k)
{
	char			min;
	long long int	n;

	n = k;
	min = '-';
	if (n < 0)
	{
		write(1, &min, 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		n = n + '0';
		write(1, &n, 1);
	}
}
