#include "inc/libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	c;

	i = 0;
	while (s[i] != 0)
	{
		c = s[i];
		write(1, &c, 1);
		i++;
	}
	write(1, "\n", 1);
}
