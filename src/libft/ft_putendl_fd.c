#include "inc/libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	c;

	i = 0;
	while (s[i] != 0)
	{
		c = s[i];
		write(fd, &c, 1);
		i++;
	}
	write(fd, "\n", 1);
}
